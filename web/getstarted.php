					The bootstrap.dat file is used to speed up your initial setup of the Unitus wallet. The file contains all of the transaction data up to the date it was published, 
					and saves your client from having to contact other nodes to sync this information.<br/>
					After you have installed your wallet, but before you start it the first time, you need to go into the application data folder:<br/>
					For Windows this will be 'C:\Users\{username}\AppData\Roaming\Unitus' - Create this if it does not already exist.<br/>
					For Linux this will be '~/.unitus' - Create this if it does not already exist.<br/>
					Extract bootstrap.dat from the file you downloaded and place it into the application data folder. <br/>
					Then start your wallet - it will import the transactions from the bootstrap.dat file into your local blockchain database (the wallet will have a status of 'Loading blocks from disk'). 
					Once the wallet has completed this process, it will then download any remaining data from other nodes. At this time the bootstrap.dat file will be renamed to 'bootstrap.dat.old' and can 
